'use strict';

var fs = require('fs');
var path = require('path');
var console = require('console');

/*eslint no-console: 0*/
function foo() {
    var filePath = path.join(__dirname, '3-node_modules.js');

    fs.readFile(filePath, 'utf8', onFile);

    function onFile(err, str) {
        if (err) {
            console.error('Could not read file: ', err);
            return;
        }

        console.log('can read file', {
            headLine: str.split('\n').slice(0, 3)
        });
    }
}

foo();
